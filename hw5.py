import json
import pathlib
from typing import Union, Tuple
import matplotlib.pyplot as plt
import numpy as np
import pandas as pd



class QuestionnaireAnalysis:
    """
    Reads and analyzes data generated by the questionnaire experiment.
    Should be able to accept strings and pathlib.Path objects.
    """

    def __init__(self, data_fname: Union[pathlib.Path, str]):
        self.data_fname = pathlib.Path(data_fname)
        if not self.data_fname.exists():
            raise ValueError(f"File {self.data_fname} does not exist")
        self.data = None

    def _require_data_loaded(self):
        if self.data is None:
            raise ValueError("Data not loaded. Call read_data() first.")

    def read_data(self):
        """Reads the json data located in self.data_fname into memory, to
        the attribute self.data.
        """
        with open(self.data_fname, 'r', encoding='utf-8') as f:
            raw_data = json.load(f)
        self.data = pd.DataFrame(raw_data).replace("nan", np.nan)

    def show_age_distrib(self) -> Tuple[np.ndarray, np.ndarray]:
        """Calculates and plots the age distribution of the participants.

        Returns
        -------
        hist : np.ndarray
          Number of people in a given bin
        bins : np.ndarray
          Bin edges
        """
        self._require_data_loaded()

        df = self.data.copy()
        df["age"] = pd.to_numeric(df["age"], errors="coerce")
        valid_ages = df["age"].dropna()
        valid_ages = valid_ages[(valid_ages >= 0) & (valid_ages <= 100)]

        bins = np.arange(0, 101, 10)
        hist, bin_edges = np.histogram(valid_ages, bins=bins)

        plt.figure()
        plt.hist(valid_ages, bins=bins, edgecolor='black')
        plt.title("Age Distribution")
        plt.xlabel("Age")
        plt.ylabel("Number of Participants")
        plt.grid(True)
        plt.savefig("age_distribution.png")
        plt.close()

        return hist, bin_edges

    def remove_rows_without_mail(self) -> pd.DataFrame:
        """Checks self.data for rows with invalid emails, and removes them.

        Returns
        -------
        df : pd.DataFrame
          A corrected DataFrame, i.e. the same table but with the erroneous rows removed and
          the (ordinal) index after a reset.
        """
        self._require_data_loaded()
        df = self.data.copy()

        def is_valid_email(email: str) -> bool:
            if not isinstance(email, str):
                return False
            if email.count('@') != 1:
                return False
            if email.startswith('@') or email.endswith('@'):
                return False
            if '.' not in email:
                return False
            if email.startswith('.') or email.endswith('.'):
                return False
            local, domain = email.split('@')
            if not domain or domain[0] == '.':
                return False
            return True

        valid_mask = df['email'].apply(is_valid_email)
        return df[valid_mask].reset_index(drop=True)

    def fill_na_with_mean(self) -> Tuple[pd.DataFrame, np.ndarray]:
        """Finds, in the original DataFrame, the subjects that didn't answer
        all questions, and replaces that missing value with the mean of the
        other grades for that student.

        Returns
        -------
        df : pd.DataFrame
          The corrected DataFrame after insertion of the mean grade
        arr : np.ndarray
          Row indices of the students that their new grades were generated
        """
        self._require_data_loaded()
        df = self.data.copy()
        question_cols = [col for col in df.columns if col.startswith('q')]

        modified_indices = []

        for idx, row in df.iterrows():
            grades = row[question_cols].astype(float)
            if grades.isnull().any():
                mean_val = grades.dropna().mean()
                df.loc[idx, question_cols] = grades.fillna(mean_val)
                modified_indices.append(idx)

        return df, np.array(modified_indices)

    def score_subjects(self, maximal_nans_per_sub: int = 1) -> pd.DataFrame:
        """Calculates the average score of a subject and adds a new "score" column
        with it.

        If the subject has more than "maximal_nans_per_sub" NaN in his grades, the
        score should be NA. Otherwise, the score is simply the mean of the other grades.
        The datatype of score is UInt8, and the floating point raw numbers should be
        rounded down.

        Parameters
        ----------
        maximal_nans_per_sub : int, optional
            Number of allowed NaNs per subject before giving a NA score.

        Returns
        -------
        pd.DataFrame
            A new DF with a new column - "score".
        """
        self._require_data_loaded()
        df = self.data.copy()
        question_cols = [col for col in df.columns if col.startswith('q')]

        scores = []
        for _, row in df.iterrows():
            grades = row[question_cols].astype(float)
            num_missing = grades.isna().sum()
            if num_missing > maximal_nans_per_sub:
                scores.append(pd.NA)
            else:
                avg = np.floor(grades.dropna().mean())
                scores.append(int(avg) if not np.isnan(avg) else pd.NA)

        df['score'] = pd.Series(scores, dtype="UInt8").where(pd.notna(scores), pd.NA)
        return df

    def correlate_gender_age(self) -> pd.DataFrame:
        """Looks for a correlation between the gender of the subject, their age
        and the score for all five questions.

        Returns
        -------
        pd.DataFrame
            A DataFrame with a MultiIndex containing the gender and whether the subject is above
            40 years of age, and the average score in each of the five questions.
        """
        self._require_data_loaded()

        df = self.data.copy()
        df["age"] = pd.to_numeric(df["age"], errors="coerce")
        df = df.dropna(subset=["age", "gender"])

        df["age"] = df["age"] > 40  # Overwrite age column with boolean for "is_above_40"

        question_cols = [col for col in df.columns if col.startswith("q")]
        grouped = df.groupby(["gender", "age"])[question_cols].mean()
        return grouped
    
